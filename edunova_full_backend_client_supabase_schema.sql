select
  fp.id,
  fi.student_id,
  fp.invoice_id,
  fp.transaction_reference as transaction_id,
  fp.amount_paid,
  fp.payment_method,
  fp.gateway,
  fp.status,
  fp.payment_date as paid_at,
  fp.receipt_url,
  fp.created_at,
  fp.updated_at
from public.fee_payments fp
join public.fee_invoices fi on fi.id = fp.invoice_id;

