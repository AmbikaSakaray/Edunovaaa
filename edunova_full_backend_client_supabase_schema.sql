create or replace view public.v_dashboard_summary as
select
  (select count(*) from public.students where deleted_at is null and status = 'Enrolled') as total_students,
  (select count(*) from public.employees where deleted_at is null and status = 'Active') as total_employees,
  (select count(*) from public.users where deleted_at is null and role = 'Teacher' and is_active = true) as total_teachers,
  (select count(*) from public.admissions where deleted_at is null and status in ('Submitted','Under Review','Shortlisted')) as pending_admissions,
  (select count(*) from public.fee_invoices where deleted_at is null and status in ('Unpaid','Overdue')) as pending_fee_invoices,
  (select count(*) from public.events where deleted_at is null and event_date >= current_date) as upcoming_events,
  (select count(*) from public.notifications where deleted_at is null) as total_notifications;

create or replace view public.v_fee_defaulters as
select
  s.id as student_id,
  s.admission_no,
  s.first_name,
  s.last_name,
  fi.id as invoice_id,
  fi.amount_due,
  fi.late_fee_accumulated,
  fi.status,
  inst.due_date
from public.fee_invoices fi
join public.students s on s.id = fi.student_id
join public.fee_installments inst on inst.id = fi.installment_id
where fi.deleted_at is null
  and fi.status in ('Unpaid','Partially Paid','Overdue')
  and inst.due_date < current_date;

